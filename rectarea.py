#Que2. WAP to get area of rectangle.

#Take input of length and breadth from user
length = int(input("Please enter length of the rectangle: "))
breadth = int(input("Please enter breadth of the rectangle: "))

#To calculate are of rectangle using formula
area = length*breadth

#Display area of rectangle of given length and breadth.
print("The area of rectangle is:",area)
