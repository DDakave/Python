#Que3. WAP to calculate average of three numbers.

#Take input of 3 numbers from user
num1 = int(input("Please enter first number:"))
num2 = int(input("Please enter second number:"))
num3 = int(input("Please enter third number:"))

#Formula to calculate average of given 3 numbers
average = (num1+num2+num3)/3

#Display average of three numbers
print("The average of",num1,",",num2,"and",num3,"is:",average)
